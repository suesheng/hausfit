/**
 * @param {string} serviceName
 * @param {string} cityName
 * @param {0 | 1 | 2} variant
 */
function comboDescriptionFromTemplate(serviceName, cityName, variant) {
  if (variant === 0) {
    return `${serviceName} in ${cityName} – HausFit NRW: Beratung, Angebot und Umsetzung aus einer Hand. Qualifizierte Handwerker für Sanierung in ${cityName} und NRW.`;
  }
  if (variant === 1) {
    return `${serviceName} ${cityName}: HausFit NRW koordiniert Planung und Montage termingerecht. Festpreise, Garantie und kostenlose Erstberatung in ${cityName}.`;
  }
  return `${serviceName} in ${cityName} mit HausFit NRW: ein Ansprechpartner, saubere Baustelle und verlässliche Abnahme. Jetzt Renovierung in ${cityName} anfragen.`;
}

/**
 * @param {string} titleKeyword
 * @param {string} cityName
 * @param {string} usp
 */
function comboTitle(titleKeyword, cityName, usp) {
  let title = `${titleKeyword} ${cityName} ✓ ${usp} | HausFit`;
  if (title.length > 60) {
    title = `${titleKeyword} ${cityName} ✓ ${usp.split(" ")[0]} | HausFit`;
  }
  if (title.length > 60) {
    title = `${titleKeyword} ${cityName} | HausFit`;
  }
  return title;
}

/**
 * @param {string} pathname
 * @returns {PageMeta | undefined}
 */
export function getRootPageMeta(pathname) {
  const normalized = pathname.replace(/\/$/, "") || "/";
  return rootPageMeta[normalized];
}

/**
 * @param {string} serviceSlug e.g. badsanierung-nrw
 * @returns {PageMeta | undefined}
 */
export function getServicePageMeta(serviceSlug) {
  return servicePageMeta[serviceSlug];
}

/**
 * @param {string} cityKey
 * @returns {PageMeta | undefined}
 */
export function getCityPageMeta(cityKey) {
  return cityPageMeta[cityKey];
}

/**
 * @param {string} serviceKey
 * @param {string} cityKey
 * @returns {PageMeta}
 */
export function getComboPageMeta(serviceKey, cityKey) {
  const overrideKey = `${serviceKey}-${cityKey}`;
  const overrideDescription = comboDescriptionOverrides[overrideKey];

  const service = comboServiceLabels[serviceKey];
  const cityName = comboCityNames[cityKey];
  if (!service || !cityName) {
    throw new Error(`Unknown combo meta: ${serviceKey}, ${cityKey}`);
  }

  const serviceIndex = serviceOrder.indexOf(serviceKey);
  const cityIndex = cityOrder.indexOf(cityKey);
  const usp = comboUsps[(serviceIndex + cityIndex) % comboUsps.length];
  const variant = /** @type {0 | 1 | 2} */ ((serviceIndex + cityIndex) % 3);

  return {
    title: comboTitle(service.titleKeyword, cityName, usp),
    description:
      overrideDescription ?? comboDescriptionFromTemplate(service.name, cityName, variant)
  };
}
